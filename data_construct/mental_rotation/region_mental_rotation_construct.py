def calculate_region_bounds(buildings: list) -> dict:
    if not buildings:
        return None
    min_x = min(b["min"][0] for b in buildings)
    max_x = max(b["max"][0] for b in buildings)
    min_y = min(b["min"][1] for b in buildings)
    max_y = max(b["max"][1] for b in buildings)
    min_z = min(b["min"][2] for b in buildings)
    max_z = max(b["max"][2] for b in buildings)
    return {
        "min_x": min_x, "max_x": max_x,
        "min_y": min_y, "max_y": max_y,
        "min_z": min_z, "max_z": max_z,
        "center_x": (min_x + max_x) / 2,
        "center_y": (min_y + max_y) / 2,
        "center_z": (min_z + max_z) / 2,
        "width": max_x - min_x,
        "depth": max_y - min_y,
        "height": max_z - min_z,
    }


def process_region(region_id: int, region_data: dict, all_buildings: list, labeler: BuildingLabeler) -> dict:
    building_ids = region_data["building_ids"]
    region_buildings = get_buildings_in_region(all_buildings, building_ids)

    if len(region_buildings) > MAX_BUILDINGS_PER_REGION:
        print(
            f"  Region {region_id}: Skipped (building count {len(region_buildings)} exceeds limit {MAX_BUILDINGS_PER_REGION})"
        )
        return None

    if len(region_buildings) < 3:
        print(f"  Region {region_id}: Skipped (insufficient buildings: {len(region_buildings)})")
        return None

    print(f"  Region {region_id}: Processing {len(region_buildings)} buildings...")

    bounds = calculate_region_bounds(region_buildings)

    labeler.set_region_visibility(building_ids)
    set_road_visibility(SHOW_ROADS_IN_REGION_RENDER)
    labeler.clear_mask_materials()
    labeler.clear_all_labels()
    apply_white_film_to_buildings(building_ids)
    if SHOW_ROADS_IN_REGION_RENDER:
        apply_road_material()
    labeler.add_building_labels(building_ids, bounds)
    set_all_labels_black()

    cam_top = labeler.setup_camera_top_down(bounds, 'Camera_Top')
    cam_iso = labeler.setup_camera_isometric(bounds, 'Camera_Iso')
    building_objs = get_region_building_objects(building_ids)
    fit_ortho_camera_to_objects(
        cam_top,
        building_objs,
        margin_ratio=TOP_FIT_MARGIN_RATIO,
        min_ortho_scale=MIN_FIT_ORTHO_SCALE,
    )
    fit_ortho_camera_to_objects(
        cam_iso,
        building_objs,
        margin_ratio=ISO_FIT_MARGIN_RATIO,
        min_ortho_scale=MIN_FIT_ORTHO_SCALE,
    )
    # Apply one global margin scale for task3 so all QA images in this region keep a consistent enlarged view.
    margin_scale = max(1.0, float(TASK3_GLOBAL_VIEW_MARGIN_SCALE))
    cam_top.data.ortho_scale = float(cam_top.data.ortho_scale) * margin_scale
    cam_iso.data.ortho_scale = float(cam_iso.data.ortho_scale) * margin_scale
    bpy.context.view_layer.update()

    simple_buildings = []
    for i, b in enumerate(region_buildings):
        simple_buildings.append({
            "label_id": i + 1,
            "original_id": b["id"],
            "center": b["center"],
            "pos": (b["center"][0], b["center"][1]),
            "half_w": b.get("half_w", b["width"] / 2),
            "half_d": b.get("half_d", b["depth"] / 2),
            "height": b["height"],
        })

    # Create output directory structure
    sample_dir = os.path.join(OUTPUT_DIR, f"region_{region_id:03d}")
    os.makedirs(sample_dir, exist_ok=True)
    ref_dir = os.path.join(sample_dir, "ref_rotations")
    os.makedirs(ref_dir, exist_ok=True)
    top_mcq_dir = os.path.join(sample_dir, "top_mcq_images")
    os.makedirs(top_mcq_dir, exist_ok=True)
    iso_mcq_dir = os.path.join(sample_dir, "isometric_mcq_images")
    os.makedirs(iso_mcq_dir, exist_ok=True)
    qa4_top_dir = os.path.join(sample_dir, "top_region_building_rotate_images")
    os.makedirs(qa4_top_dir, exist_ok=True)
    qa4_iso_dir = os.path.join(sample_dir, "iso_region_building_rotate_images")
    os.makedirs(qa4_iso_dir, exist_ok=True)

    # Get building objects from Blender
    building_objs = []
    id_to_obj = {}
    for i, bid in enumerate(building_ids):
        obj = bpy.data.objects.get(bid)
        if obj:
            building_objs.append(obj)
            id_to_obj[i + 1] = obj
            # Ensure object origin is at geometric center (for correct rotation)
            if obj.type == 'MESH' and obj.data:
                # Set origin to geometric center via Blender API
                bpy.ops.object.select_all(action='DESELECT')
                obj.select_set(True)
                bpy.context.view_layer.objects.active = obj
                bpy.ops.object.origin_set(type='ORIGIN_GEOMETRY', center='MEDIAN')

    if len(building_objs) < 2:
        print(f"    Warning: insufficient building objects")
        labeler.show_all_visible_objects()
        return None

    # ===== Select target and other building =====
    ids = list(range(1, len(building_objs) + 1))
    target_id = random.choice(ids)
    other_id = random.choice([i for i in ids if i != target_id])
    target_obj = id_to_obj[target_id]
    other_obj = id_to_obj[other_id]
    target_meta = simple_buildings[target_id - 1]
    other_meta = simple_buildings[other_id - 1]

    # ===== Task 1: Will rotation cause overlap? =====
    dir1 = random.choice(ROT_DIR_POOL)
    angle1 = random.choice(ROT_ANGLE_POOL)
    signed1 = _rotate_dir_to_signed_deg(dir1, angle1)
    rel1 = _relation_after_rotation(target_obj, target_meta, other_obj, other_meta, signed1, COLLISION_PAD)
    overlap1 = (rel1 == "COLLIDE")

    qa1 = qa_task1_overlap_yesno(sample_dir, target_id, other_id, dir1, angle1)
    qa1["answer"] = "Yes" if overlap1 else "No"

    # Render QA1 reference images
    clear_labels_only()
    labeler.add_building_labels(building_ids, bounds)
    set_all_labels_black()
    pose_q1 = _capture_obj_pose(target_obj)
    try:
        _set_building_rotation_deg_about_bbox_center(target_obj, signed1)
        force_disable_all_shadows()
        render_view(
            cam_top,
            os.path.join(ref_dir, f"qa1_ref_{dir1}_{angle1:03d}.png"),
            building_objs=building_objs,
            isometric=False,
        )
    finally:
        _restore_obj_pose(target_obj, pose_q1)

    # ===== Task 2: Minimum angle to avoid overlap =====
    task2_result = _pick_task2_direction_and_options(target_obj, target_meta, other_obj, other_meta)
    if task2_result:
        dir2, options_deg, correct_deg = task2_result
        qa2 = qa_task2_avoid_collision_angle_mcq(sample_dir, target_id, other_id, dir2, options_deg, correct_deg)

        # Render QA2 reference images
        for deg in options_deg:
            signed = _rotate_dir_to_signed_deg(dir2, deg)
            clear_labels_only()
            labeler.add_building_labels(building_ids, bounds)
            set_all_labels_black()
            pose_q2 = _capture_obj_pose(target_obj)
            try:
                _set_building_rotation_deg_about_bbox_center(target_obj, signed)
                force_disable_all_shadows()
                render_view(
                    cam_top,
                    os.path.join(ref_dir, f"qa2_ref_{dir2}_{deg:03d}.png"),
                    building_objs=building_objs,
                    isometric=False,
                )
            finally:
                _restore_obj_pose(target_obj, pose_q2)
    else:
        qa2 = None

    # ===== Task 3: Rotation image matching (Top view) =====
    # Generate 4 options (different rotation angles) to ensure distinct visual outcomes
    def _get_unique_self_rotation_options(direction):
        """Get angle options that produce distinct visual outcomes (unique modulo 360)."""
        max_attempts = 100
        for _ in range(max_attempts):
            candidate_degs = random.sample(ROT_ANGLE_POOL, ROT_IMG_MCQ_K)
            # Normalize angles into [0, 360)
            normalized = set(deg % 360 for deg in candidate_degs)
            # Check whether all are unique
            if len(normalized) == ROT_IMG_MCQ_K:
                return candidate_degs
        # If repeated attempts fail, keep candidates and print a warning
        print(f"    Warning: could not find fully distinct rotation angles")
        return candidate_degs

    dir3 = random.choice(ROT_DIR_POOL)
    option_degs = _get_unique_self_rotation_options(dir3)
    query_deg = random.choice(option_degs)

    option_files = []
    correct_file = None

    for deg in option_degs:
        signed = _rotate_dir_to_signed_deg(dir3, deg)
        clear_labels_only()
        pose_q3 = _capture_obj_pose(target_obj)
        try:
            _set_building_rotation_deg_about_bbox_center(target_obj, signed)
            labeler.add_building_labels(building_ids, bounds)
            set_all_labels_black()
            force_disable_all_shadows()
            fn = f"rot_{dir3}_{deg:03d}.png"
            rel_fp = os.path.join("top_mcq_images", fn)
            render_view(
                cam_top,
                os.path.join(sample_dir, rel_fp),
                building_objs=building_objs,
                isometric=False,
            )
        finally:
            _restore_obj_pose(target_obj, pose_q3)
        option_files.append(rel_fp)
        if deg == query_deg:
            correct_file = rel_fp

    if correct_file is None:
        qa3 = None
    else:
        qa3 = qa_task3_rotation_image_mcq(sample_dir, target_id, dir3, query_deg, option_files, correct_file, view="top")

    # ===== Task 3: Rotation image matching (Isometric view) =====
    option_files_iso = []
    correct_file_iso = None

    for deg in option_degs:
        signed = _rotate_dir_to_signed_deg(dir3, deg)
        clear_labels_only()
        pose_q3_iso = _capture_obj_pose(target_obj)
        try:
            _set_building_rotation_deg_about_bbox_center(target_obj, signed)
            labeler.add_building_labels(building_ids, bounds)
            set_all_labels_black()
            force_disable_all_shadows()
            fn = f"iso_rot_{dir3}_{deg:03d}.png"
            rel_fp = os.path.join("isometric_mcq_images", fn)
            render_view(
                cam_iso,
                os.path.join(sample_dir, rel_fp),
                building_objs=building_objs,
                isometric=True,
            )
        finally:
            _restore_obj_pose(target_obj, pose_q3_iso)
        option_files_iso.append(rel_fp)
        if deg == query_deg:
            correct_file_iso = rel_fp

    if correct_file_iso is None:
        qa_iso = None
    else:
        qa_iso = qa_task3_rotation_image_mcq(sample_dir, target_id, dir3, query_deg, option_files_iso, correct_file_iso, view="isometric")

    # ===== Render top.png and isometric.png =====
    print(f"    Rendering top.png...")
    clear_labels_only()
    _reset_building_rotation(target_obj)
    labeler.add_building_labels(building_ids, bounds)
    set_all_labels_black()
    force_disable_all_shadows()
    top_path = os.path.join(sample_dir, "top.png")
    top_render_meta = render_view(
        cam_top,
        top_path,
        building_objs=building_objs,
        isometric=False,
    )

    print(f"    Rendering isometric.png...")
    clear_labels_only()
    _reset_building_rotation(target_obj)
    labeler.add_building_labels(building_ids, bounds)
    set_all_labels_black()
    force_disable_all_shadows()
    iso_path = os.path.join(sample_dir, "isometric.png")
    render_view(
        cam_iso,
        iso_path,
        building_objs=building_objs,
        isometric=True,
    )

    # ===== Task 4: Rotate around region center (Top view) =====
    print(f"    Generating QA4 (rotation around region center)...")
    # Select a different target building for QA4
    all_ids = list(range(1, len(building_objs) + 1))
    qa4_target_id = random.choice([i for i in all_ids if i != target_id])
    qa4_target_obj = id_to_obj[qa4_target_id]
    qa4_target_meta = simple_buildings[qa4_target_id - 1]

    # Region center: back-project the geometric center of final top image (including N panel) to world coordinates
    region_center_ctx = _compute_top_center_world_from_render_meta(cam_top, top_render_meta)
    if region_center_ctx is not None:
        rcx, rcy = region_center_ctx["center_world_xy"]
        region_center = Vector((float(rcx), float(rcy), float(bounds["center_z"])))
    else:
        region_center = Vector((bounds["center_x"], bounds["center_y"], bounds["center_z"]))
    # Save original position
    original_loc = qa4_target_obj.location.copy()

    # Randomly choose rotation direction
    dir4 = random.choice(ROT_DIR_POOL)

    # Generate 4 options (different rotation angles) to ensure distinct positions
    def _get_unique_rotation_options(direction, target_obj, region_center, target_pos):
        """Get rotation-angle options that produce distinct positions."""
        max_attempts = 100
        for _ in range(max_attempts):
            candidate_degs = random.sample(ROT_ANGLE_POOL, ROT_IMG_MCQ_K)
            # Compute position after rotation for each angle
            positions = []
            for deg in candidate_degs:
                signed = _rotate_dir_to_signed_deg(direction, deg)
                # Compute rotated position
                rel_vec = target_pos - region_center
                angle_rad = math.radians(signed)
                cos_a = math.cos(angle_rad)
                sin_a = math.sin(angle_rad)
                new_x = region_center.x + rel_vec.x * cos_a - rel_vec.y * sin_a
                new_y = region_center.y + rel_vec.x * sin_a + rel_vec.y * cos_a
                positions.append((new_x, new_y))
            # Check whether all positions are unique
            if len(set(positions)) == ROT_IMG_MCQ_K:
                return candidate_degs
        # If repeated attempts fail, keep candidates and print a warning
        print(f"    Warning: could not find fully distinct positions; using candidate angles")
        return candidate_degs

    # Get unique position angles
    target_original_pos = Vector((qa4_target_obj.location.x, qa4_target_obj.location.y, 0))
    option_degs4 = _get_unique_rotation_options(dir4, qa4_target_obj, region_center, target_original_pos)
    query_deg4 = random.choice(option_degs4)

    option_files_qa4 = []
    correct_file_qa4 = None

    for deg in option_degs4:
        signed_deg = _rotate_dir_to_signed_deg(dir4, deg)
        clear_labels_only()
        # Rotate building first
        _rotate_building_around_point(qa4_target_obj, region_center, signed_deg)
        # Then add labels via labeler (labels automatically follow building positions)
        labeler.add_building_labels(building_ids, bounds)
        set_all_labels_black()
        force_disable_all_shadows()
        fn = f"rot_center_{dir4}_{deg:03d}.png"
        rel_fp = os.path.join("top_region_building_rotate_images", fn)
        render_view(
            cam_top,
            os.path.join(sample_dir, rel_fp),
            building_objs=building_objs,
            isometric=False,
        )
        # Reset position and rotation
        _reset_building_rotation_and_position(qa4_target_obj, original_loc)
        option_files_qa4.append(rel_fp)
        if deg == query_deg4:
            correct_file_qa4 = rel_fp

    if correct_file_qa4:
        qa4 = qa_task4_rotation_position_mcq(sample_dir, qa4_target_id, dir4, query_deg4, option_files_qa4, correct_file_qa4, view="top")
    else:
        qa4 = None

    # ===== Task 4: Rotate around region center (Isometric view) =====
    option_files_qa4_iso = []
    correct_file_qa4_iso = None

    for deg in option_degs4:
        signed_deg = _rotate_dir_to_signed_deg(dir4, deg)
        clear_labels_only()
        # Rotate building first
        _rotate_building_around_point(qa4_target_obj, region_center, signed_deg)
        # Then add labels via labeler (labels automatically follow building positions)
        labeler.add_building_labels(building_ids, bounds)
        set_all_labels_black()
        force_disable_all_shadows()
        fn = f"iso_rot_center_{dir4}_{deg:03d}.png"
        rel_fp = os.path.join("iso_region_building_rotate_images", fn)
        render_view(
            cam_iso,
            os.path.join(sample_dir, rel_fp),
            building_objs=building_objs,
            isometric=True,
        )
        _reset_building_rotation_and_position(qa4_target_obj, original_loc)
        option_files_qa4_iso.append(rel_fp)
        if deg == query_deg4:
            correct_file_qa4_iso = rel_fp

    if correct_file_qa4_iso:
        qa4_iso = qa_task4_rotation_position_mcq(sample_dir, qa4_target_id, dir4, query_deg4, option_files_qa4_iso, correct_file_qa4_iso, view="isometric")
    else:
        qa4_iso = None

    # Cleanup
    labeler.clear_all_labels()
    labeler.clear_mask_materials()
    labeler.show_all_visible_objects()

    # Assemble QA tasks
    qa_top = [qa1]
    if qa2:
        qa_top.append(qa2)
    if qa3:
        qa_top.append(qa3)
    if qa4:
        qa_top.append(qa4)

    qa_iso = [qa_iso] if qa_iso else []
    if qa4_iso:
        qa_iso.append(qa4_iso)

    return {
        "region_id": region_id,
        "building_count": len(region_buildings),
        "buildings": simple_buildings,
        "images": {
            "top": os.path.abspath(top_path),
            "isometric": os.path.abspath(iso_path),
        },
        "qa": {"top": qa_top, "isometric": qa_iso},
        "special_refs": {
            "target_id": target_id,
            "other_id": other_id,
            "qa4_target_id": qa4_target_id,
            "qa12_self_rotation_center": "building_bbox_center",
            "qa12_collision_mode": "2D_exact_mesh_projection_for_COLLIDE+OBB_pad_for_NEAR",
            "scene_center_definition": "final top-image geometric center (including north panel)",
            "scene_center_world_xy": [float(region_center.x), float(region_center.y)],
        },
    }


def main():
    max_regions = MAX_REGIONS_ARG
    if max_regions is None:
        max_regions = int(os.environ.get("MAX_REGIONS", "0"))
    use_clean = USE_CLEAN_ARG == 1
    if "USE_CLEAN_SCENE" in os.environ:
        use_clean = os.environ.get("USE_CLEAN_SCENE", "1").lower() in ("1", "true", "yes")

    effective_blend_path, effective_region_json_path, failed_region_ids, using_clean = resolve_region_inputs(
        BLEND_PATH,
        REGION_DIR,
        use_clean=use_clean,
    )

    print("=" * 60)
    print(f"Region mental-rotation task generation - Region: {REGION_NAME}")
    print("=" * 60)

    os.makedirs(OUTPUT_DIR, exist_ok=True)

    print("\n[1/4] Loading data...")
    print(f"  Region: {REGION_NAME}")
    print(f"  BLEND file: {effective_blend_path}")
    print(f"  Region directory: {REGION_DIR}")
    print(f"  Region JSON: {effective_region_json_path}")
    print(f"  Using clean inputs: {using_clean}")
    print(f"  Output directory: {OUTPUT_DIR}")

    all_buildings = load_buildings_from_blend(effective_blend_path)
    print(f"  Loaded buildings: {len(all_buildings)}")

    region_data = load_region_data(effective_region_json_path)
    regions = region_data.get("regions", [])
    raw_region_count = len(regions)
    if failed_region_ids:
        regions = [r for r in regions if int(r.get("region_id", -1)) not in failed_region_ids]
        print(f"  After filtering failed_regions: {len(regions)}/{raw_region_count} regions")
    if max_regions <= 0:
        regions = regions
    else:
        regions = regions[:max_regions]
    print(f"  Region count: {len(regions)}")

    print("\n[2/4] Opening BLEND file...")
    bpy.ops.wm.open_mainfile(filepath=effective_blend_path)
    render_resolution = (RESOLUTION, RESOLUTION) if RESOLUTION > 0 else _render_resolution_px()

    labeler = BuildingLabeler(
        region_data_path=effective_region_json_path,
        blend_path=effective_blend_path,
        output_dir=OUTPUT_DIR,
        ortho_scale_factor=1.3,
        label_height_ratio=0.01,
        font_size_ratio=0.07,
        samples=32,
        resolution=render_resolution,
        mask_alpha=0.8,
    )

    _original_setup_lighting = labeler.setup_lighting

    def _patched_setup_lighting(self):
        scene = bpy.context.scene
        for obj in list(bpy.data.objects):
            if obj.type == 'LIGHT':
                bpy.data.objects.remove(obj)

        sun = bpy.data.objects.new("Sun", bpy.data.lights.new("SunLight", type='SUN'))
        bpy.context.collection.objects.link(sun)
        sun.data.energy = 4.0
        sun.rotation_euler = (math.radians(45), math.radians(15), math.radians(30))
        if hasattr(sun.data, 'cast_shadow'):
            sun.data.cast_shadow = False

        fill_light = bpy.data.objects.new("FillLight", bpy.data.lights.new("FillLight", type='AREA'))
        bpy.context.collection.objects.link(fill_light)
        fill_light.data.energy = 150
        fill_light.location = (0, 0, 100)
        fill_light.data.size = 100
        if hasattr(fill_light.data, 'cast_shadow'):
            fill_light.data.cast_shadow = False

        scene.cycles.use_shadows = False
        scene.cycles.use_progressive = False
        scene.cycles.use_shadow_highlight = False
        scene.cycles.blur_shadow = 0

        for obj in bpy.data.objects:
            if hasattr(obj, 'cycles_visibility'):
                obj.cycles_visibility.cast_shadow = False
                obj.cycles_visibility.receive_shadow = False

    labeler.setup_lighting = types.MethodType(_patched_setup_lighting, labeler)

    labeler.setup_render()
    labeler.setup_lighting()
    force_disable_all_shadows()

    print("\n[3/4] Processing regions...")
    all_results = []
    valid_count = 0

    for region in regions:
        region_id = region["region_id"]
        result = process_region(region_id, region, all_buildings, labeler)
        if result:
            all_results.append(result)
            valid_count += 1

    print(f"\n  Successfully processed {valid_count}/{len(regions)} regions")

    print("\n[4/4] Saving metadata...")
    meta_path = os.path.join(OUTPUT_DIR, "metadata_regions.json")
    with open(meta_path, "w", encoding="utf-8") as f:
        json.dump(all_results, f, ensure_ascii=False, indent=2)

    print(f"\nDone!")
    print(f"  Output directory: {OUTPUT_DIR}")
    print(f"  Metadata file: {meta_path}")
    print(f"  Valid regions: {valid_count}")


if __name__ == "__main__":
    main()
