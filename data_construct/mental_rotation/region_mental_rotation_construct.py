def projected_building_bbox_pixels(cam, objects: list) -> tuple[int, int, int, int] | None:
    if cam is None or cam.type != "CAMERA":
        return None
    scene = bpy.context.scene
    rx, ry = _render_resolution_px()
    xs = []
    ys = []
    for obj in objects:
        for p in _iter_object_world_vertices(obj):
            ndc = world_to_camera_view(scene, cam, p)
            x = float(ndc.x) * rx
            y = (1.0 - float(ndc.y)) * ry
            xs.append(x)
            ys.append(y)
    if not xs or not ys:
        return None
    x1 = max(0, min(rx - 1, int(math.floor(min(xs)))))
    y1 = max(0, min(ry - 1, int(math.floor(min(ys)))))
    x2 = max(0, min(rx - 1, int(math.ceil(max(xs)))))
    y2 = max(0, min(ry - 1, int(math.ceil(max(ys)))))
    if x2 <= x1 or y2 <= y1:
        return None
    return (x1, y1, x2, y2)


def _compute_reframe_crop_bounds_from_bbox(
    image_w: int,
    image_h: int,
    building_bbox: tuple[int, int, int, int] | None,
    pad_ratio: float,
) -> tuple[int, int, int, int]:
    if building_bbox is None:
        return (0, 0, int(image_w), int(image_h))

    x1 = max(0, min(image_w - 1, int(building_bbox[0])))
    y1 = max(0, min(image_h - 1, int(building_bbox[1])))
    x2 = max(0, min(image_w - 1, int(building_bbox[2])))
    y2 = max(0, min(image_h - 1, int(building_bbox[3])))
    if x2 < x1:
        x1, x2 = x2, x1
    if y2 < y1:
        y1, y2 = y2, y1

    bw = max(1, x2 - x1 + 1)
    bh = max(1, y2 - y1 + 1)
    pad_x = int(round(bw * float(pad_ratio)))
    pad_y = int(round(bh * float(pad_ratio)))

    x1 = max(0, x1 - pad_x)
    y1 = max(0, y1 - pad_y)
    x2 = min(image_w - 1, x2 + pad_x)
    y2 = min(image_h - 1, y2 + pad_y)
    return (int(x1), int(y1), int(x2 + 1), int(y2 + 1))


def _pixel_to_world_xy_in_top(cam, px: float, py: float, rx: int, ry: int) -> tuple[float, float]:
    cam_axes = cam.matrix_world.to_3x3()
    cam_right = (cam_axes @ Vector((1.0, 0.0, 0.0))).normalized()
    cam_up = (cam_axes @ Vector((0.0, 1.0, 0.0))).normalized()
    world_per_px = float(cam.data.ortho_scale) / float(max(1, rx))
    dx = (float(px) - float(rx) * 0.5) * world_per_px
    dy = (float(ry) * 0.5 - float(py)) * world_per_px
    wp = cam.location + cam_right * dx + cam_up * dy
    return float(wp.x), float(wp.y)


def _compute_top_center_world_from_render_meta(cam, render_meta: dict | None) -> dict | None:
    if not isinstance(render_meta, dict):
        return None
    reframe_crop = render_meta.get("reframe_crop")
    north_meta = render_meta.get("north_meta") or {}
    trim_meta = render_meta.get("trim_meta") or {}
    rx = int(render_meta.get("render_w", 0))
    ry = int(render_meta.get("render_h", 0))
    if not (isinstance(reframe_crop, (list, tuple)) and len(reframe_crop) == 4):
        return None
    if not isinstance(trim_meta, dict):
        return None

    final_w = float(trim_meta.get("final_w", 0.0))
    final_h = float(trim_meta.get("final_h", 0.0))
    if final_w <= 0 or final_h <= 0 or rx <= 0 or ry <= 0:
        return None

    final_cx = final_w * 0.5
    final_cy = final_h * 0.5
    reframe_x1, reframe_y1 = int(reframe_crop[0]), int(reframe_crop[1])
    trim_x1 = int(trim_meta.get("crop_x1", 0))
    trim_y1 = int(trim_meta.get("crop_y1", 0))
    ext_top = int(north_meta.get("ext_top", 0))

    render_cx = float(final_cx) + float(trim_x1) + float(reframe_x1)
    render_cy = float(final_cy) + float(trim_y1) - float(ext_top) + float(reframe_y1)
    world_cx, world_cy = _pixel_to_world_xy_in_top(cam, render_cx, render_cy, rx, ry)

    return {
        "center_final_px": (float(final_cx), float(final_cy)),
        "center_render_px": (float(render_cx), float(render_cy)),
        "center_world_xy": (float(world_cx), float(world_cy)),
    }


