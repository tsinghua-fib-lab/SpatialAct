def add_label(building_obj, label_text: str, cam, font_size: float = None, building_height: float = None, region_max_dim: float = None) -> None:
    """Add a label to a building

    Args:
        building_obj: Blender building object
        label_text: label text
        cam: camera object
        font_size: font size; auto-computed from building size when None
        building_height: building height, used for label placement
        region_max_dim: region max dimension, used for label placement
    """
    # Get building bounds
    world_matrix = building_obj.matrix_world
    mesh = building_obj.data
    if not mesh:
        return

    if hasattr(building_obj, 'bound_box') and building_obj.bound_box:
        verts = [world_matrix @ Vector(co) for co in building_obj.bound_box]
    else:
        verts = [world_matrix @ v.co for v in mesh.vertices]

    xs = [v.x for v in verts]
    ys = [v.y for v in verts]
    zs = [v.z for v in verts]

    center_x = (min(xs) + max(xs)) / 2
    center_y = (min(ys) + max(ys)) / 2
    center_z = (min(zs) + max(zs)) / 2
    max_z = max(zs)

    # Compute font size
    bounds_width = max(xs) - min(xs)
    bounds_depth = max(ys) - min(ys)
    max_dim = max(bounds_width, bounds_depth)
    if font_size is None:
        font_size = max_dim * 0.4

    text_curve = bpy.data.curves.new(f"Label_{label_text}_curve", type="FONT")
    text_curve.body = str(label_text)
    text_curve.size = font_size
    text_curve.align_x = "CENTER"
    text_curve.align_y = "CENTER"
    text_curve.extrude = max_dim * 0.01
    text_curve.fill_mode = "BOTH"

    text_obj = bpy.data.objects.new(f"Label_{label_text}", text_curve)
    bpy.context.collection.objects.link(text_obj)

    # Label position: above building top (consistent with task2_spatial_orientation)
    if building_height is not None and region_max_dim is not None:
        # Offset by half building height + 3% of region size
        label_z = center_z + building_height / 2 + region_max_dim * 0.03
    else:
        label_z = max_z + font_size * 0.3

    text_obj.location = (center_x, center_y, label_z)

    text_obj.visible_shadow = False
    if hasattr(text_obj, "cycles_visibility"):
        text_obj.cycles_visibility.shadow = False
        text_obj.cycles_visibility.diffuse = False
        text_obj.cycles_visibility.glossy = False
        text_obj.cycles_visibility.ambient_occlusion = False

    billboard = text_obj.constraints.new(type="LOCKED_TRACK")
    billboard.target = cam
    billboard.track_axis = "TRACK_Z"
    billboard.lock_axis = "LOCK_Y"

    lock_roll = text_obj.constraints.new(type="LIMIT_ROTATION")
    lock_roll.owner_space = "LOCAL"
    lock_roll.use_limit_x = False
    lock_roll.use_limit_y = False
    lock_roll.use_limit_z = True
    lock_roll.min_z = 0.0
    lock_roll.max_z = 0.0

    mat = create_black_label_material(f"Label_{label_text}_mat")
    text_obj.data.materials.clear()
    text_obj.data.materials.append(mat)

    bpy.context.view_layer.update()


def render_view(
    cam,
    output_path: str,
    building_objs: list[bpy.types.Object] | None = None,
    isometric: bool = False,
    north_world_dir: Vector = Vector((0.0, 1.0, 0.0)),
    refit_camera: bool = False,
) -> dict | None:
    # Refit can be enabled for rotated option images to keep the target building in frame.
    if refit_camera and building_objs:
        fit_ortho_camera_to_objects(
            cam,
            building_objs,
            margin_ratio=ISO_FIT_MARGIN_RATIO if isometric else TOP_FIT_MARGIN_RATIO,
            min_ortho_scale=MIN_FIT_ORTHO_SCALE,
        )
        bpy.context.view_layer.update()

    scene = bpy.context.scene
    scene.camera = cam
    scene.render.filepath = output_path
    bpy.ops.render.render(write_still=True)

    meta = None
    if building_objs:
        render_rx, render_ry = _render_resolution_px()
        bbox = projected_building_bbox_pixels(cam, building_objs)
        arrow_size = ISO_NORTH_ARROW_SIZE if isometric else TOP_NORTH_ARROW_SIZE
        if TASK3_DYNAMIC_POST_CROP:
            reframe_crop = _compute_reframe_crop_bounds_from_bbox(
                render_rx, render_ry, bbox, REFRAME_PAD_RATIO
            )
            reframe_image_focus_buildings(
                output_path,
                arrow_size=arrow_size,
                reserve_for_north=True,
                building_bbox=bbox,
            )
        else:
            reframe_crop = (0, 0, int(render_rx), int(render_ry))

        north_meta = draw_north_arrow(
            output_path,
            cam=cam,
            north_world_dir=north_world_dir,
            arrow_size=arrow_size,
            corner_idx=0,
            return_meta=True,
        )
        if TASK3_DYNAMIC_POST_CROP:
            trim_meta = trim_image_to_alpha_bbox(output_path, pad_px=FINAL_TRIM_PAD_PX, return_meta=True)
        else:
            nw = int((north_meta or {}).get("final_w", render_rx))
            nh = int((north_meta or {}).get("final_h", render_ry))
            trim_meta = {
                "ok": True,
                "crop_x1": 0,
                "crop_y1": 0,
                "crop_x2": int(nw),
                "crop_y2": int(nh),
                "final_w": int(nw),
                "final_h": int(nh),
            }
        meta = {
            "render_w": int(render_rx),
            "render_h": int(render_ry),
            "building_bbox": bbox,
            "reframe_crop": reframe_crop,
            "north_meta": north_meta,
            "trim_meta": trim_meta,
        }
    return meta


