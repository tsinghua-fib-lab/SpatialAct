def in_bounds_center(x: float, y: float, bounds: dict, margin: float = 0.5) -> bool:
    return (
        (bounds["min_x"] + margin) <= x <= (bounds["max_x"] - margin) and
        (bounds["min_y"] + margin) <= y <= (bounds["max_y"] - margin)
    )


def nearest_neighbor_id(ids: list[int], metas: list[dict], pivot_id: int) -> int:
    px, py = metas[pivot_id - 1]["pos"]
    best = None
    best_d = 1e30
    for i in ids:
        if i == pivot_id:
            continue
        qx, qy = metas[i - 1]["pos"]
        d = (px - qx) ** 2 + (py - qy) ** 2
        if d < best_d:
            best_d = d
            best = i
    return int(best) if best is not None else int(random.choice([i for i in ids if i != pivot_id]))


def process_region(
    region_id: int,
    region: dict,
    labeler: BuildingLabeler,
    sample_idx: int,
    variant_idx: int = 1,
    variant_total: int = 1,
) -> dict | None:
    building_ids = region["building_ids"]
    objs = []
    id_to_obj = {}

    # Collect objects in blender
    for i, bid in enumerate(building_ids):
        o = bpy.data.objects.get(bid)
        if o and o.type == "MESH":
            objs.append(o)
            id_to_obj[i + 1] = o

    if len(objs) > MAX_BUILDINGS_PER_REGION:
        print(
            f"[Region {region_id}] skip: {len(objs)} mesh buildings exceed limit {MAX_BUILDINGS_PER_REGION}"
        )
        return None

    if len(objs) < 3:
        print(f"[Region {region_id}] skip: only {len(objs)} mesh buildings")
        return None

    # Ensure these objects are movable by location (bake if needed)
    ensure_buildings_movable(objs)

    # Build metas from actual objects
    metas = []
    for i in range(len(objs)):
        metas.append(build_meta_from_obj(objs[i], label_id=i + 1))

    # Bounds
    bounds = compute_scene_bounds(objs)
    region_max_dim = max(bounds["width"], bounds["depth"])
    label_font_size = float(region_max_dim) * LABEL_SIZE_RATIO

    # Setup region visibility/masks/roads with labeler
    labeler.set_region_visibility(building_ids)
    set_road_visibility(SHOW_ROADS_IN_REGION_RENDER)
    labeler.clear_mask_materials()
    labeler.clear_all_labels()
    apply_white_film_to_buildings(building_ids)
    if SHOW_ROADS_IN_REGION_RENDER:
        apply_road_material()

    cam_top = labeler.setup_camera_top_down(bounds, "Camera_Top")
    fit_ortho_camera_to_objects(
        cam_top,
        objs,
        margin_ratio=TOP_FIT_MARGIN_RATIO,
        min_ortho_scale=MIN_FIT_ORTHO_SCALE,
    )

    # Output dirs
    if int(variant_total) > 1:
        sample_dir = os.path.join(OUTPUT_DIR, f"region_{region_id:03d}_v{int(variant_idx):02d}")
    else:
        sample_dir = os.path.join(OUTPUT_DIR, f"region_{region_id:03d}")
    os.makedirs(sample_dir, exist_ok=True)
    ref_dir = os.path.join(sample_dir, "ref_images")
    os.makedirs(ref_dir, exist_ok=True)

    ids = list(range(1, len(objs) + 1))

    def _with_center_overrides(pos_overrides: dict[int, tuple[float, float]] | None, fn):
        if not pos_overrides:
            return fn()
        saved = {}
        try:
            for idx, new_center in pos_overrides.items():
                if idx < 0 or idx >= len(objs):
                    continue
                obj = objs[idx]
                saved[idx] = obj.location.copy()
                old_cx, old_cy = metas[idx]["obb"]["center"][:2]
                dx = float(new_center[0] - old_cx)
                dy = float(new_center[1] - old_cy)
                obj.location = (float(obj.location.x + dx), float(obj.location.y + dy), float(obj.location.z))
            bpy.context.view_layer.update()
            return fn()
        finally:
            for idx, loc in saved.items():
                objs[idx].location = loc
            if saved:
                bpy.context.view_layer.update()

    def _exact_pair_collide(idx_a: int, idx_b: int, pos_overrides: dict[int, tuple[float, float]] | None = None) -> bool:
        pos_a = pos_overrides.get(idx_a) if pos_overrides else None
        pos_b = pos_overrides.get(idx_b) if pos_overrides else None

        def _calc() -> bool:
            if not aabb_overlap_xy_by_meta(metas[idx_a], metas[idx_b], inflate=0.0, pos_a=pos_a, pos_b=pos_b):
                return False
            return exact_mesh_overlap_xy(objs[idx_a], objs[idx_b])

        return bool(_with_center_overrides(pos_overrides, _calc))

    def _exact_scene_collision_pairs(pos_overrides: dict[int, tuple[float, float]] | None = None) -> list[tuple[int, int]]:
        def _calc() -> list[tuple[int, int]]:
            pairs = []
            n = len(metas)
            for i in range(n):
                for j in range(i + 1, n):
                    pos_i = pos_overrides.get(i) if pos_overrides else None
                    pos_j = pos_overrides.get(j) if pos_overrides else None
                    if not aabb_overlap_xy_by_meta(metas[i], metas[j], inflate=0.0, pos_a=pos_i, pos_b=pos_j):
                        continue
                    if exact_mesh_overlap_xy(objs[i], objs[j]):
                        pairs.append((i + 1, j + 1))
            return pairs

        return _with_center_overrides(pos_overrides, _calc)

    # ----------------------------------------------------------
    # QA1 move (strict -> flip)
    # ----------------------------------------------------------
    desired_move_collide = (sample_idx % 2 == 0)
    move_task = None
    move_ref = None  # (move_id, nx, ny)

    def _pick_move(mode: str):
        for _ in range(MOVE_TASK_TRIES):
            move_id = random.choice(ids)
            other_id = nearest_neighbor_id(ids, metas, move_id)

            meta_m = metas[move_id - 1]
            meta_o = metas[other_id - 1]
            old_x, old_y = meta_m["obb"]["center"][:2]
            before_rel = relation_2d_with_pad(meta_m, meta_o, pad=COLLISION_PAD, moved_pos=None)
            if before_rel == "NEAR":
                continue
            before_collide_fast = (before_rel == "COLLIDE")

            dirs = MOVE_DIR_POOL[:]
            dists = MOVE_DIST_POOL[:]
            random.shuffle(dirs)
            random.shuffle(dists)

            for d in dirs:
                vx, vy = _move_dir_to_vec(d)
                for dist in dists:
                    actual_dist = dist * UNIT_SCALE
                    nx = float(old_x + vx * actual_dist)
                    ny = float(old_y + vy * actual_dist)
                    if not in_bounds_center(nx, ny, bounds, margin=IN_BOUNDS_MARGIN):
                        continue

                    rel = relation_2d_with_pad(meta_m, meta_o, pad=COLLISION_PAD, moved_pos=(nx, ny))
                    if rel == "NEAR":
                        continue
                    after_collide_fast = (rel == "COLLIDE")

                    if mode == "strict":
                        if after_collide_fast != desired_move_collide:
                            continue
                    elif mode == "flip":
                        if after_collide_fast == before_collide_fast:
                            continue
                    else:
                        continue

                    move_idx = move_id - 1
                    other_idx = other_id - 1
                    pos_overrides = {move_idx: (nx, ny)}
                    before_collide_exact = _exact_pair_collide(move_idx, other_idx, pos_overrides=None)
                    after_collide_exact = _exact_pair_collide(move_idx, other_idx, pos_overrides=pos_overrides)

                    # Final exact check before persisting answer; mismatch -> keep searching.
                    if after_collide_exact != after_collide_fast:
                        continue
                    if before_collide_exact != before_collide_fast:
                        continue

                    if mode == "strict":
                        if after_collide_exact != desired_move_collide:
                            continue
                    elif mode == "flip":
                        if after_collide_exact == before_collide_exact:
                            continue
                    else:
                        continue

                    task = qa_task_move_collision_yesno(sample_dir, move_id, other_id, d, int(dist), after_collide_exact)
                    return task, (move_id, nx, ny)

        return None, None

    move_task, move_ref = _pick_move("strict")
    if move_task is None:
        move_task, move_ref = _pick_move("flip")

    # ----------------------------------------------------------
    # QA2 swap (alternate desired_after_collide)
    # ----------------------------------------------------------
    desired_after_collide = (sample_idx % 2 == 1)

    before_pairs = collision_pairs_in_scene(metas)
    before_collide_fast = (len(before_pairs) > 0)
    before_pairs_exact = _exact_scene_collision_pairs()
    before_collide = (len(before_pairs_exact) > 0)

    swap_task = None
    swap_ref = None  # (a_id, b_id)

    all_pairs = list(combinations(ids, 2))
    random.shuffle(all_pairs)

    def _pick_swap(mode: str):
        for a_id, b_id in all_pairs:
            pa = tuple(metas[a_id - 1]["obb"]["center"][:2])
            pb = tuple(metas[b_id - 1]["obb"]["center"][:2])

            pos_overrides = {
                a_id - 1: pb,
                b_id - 1: pa,
            }

            after_pairs = collision_pairs_in_scene(metas, pos_overrides=pos_overrides)
            after_collide_fast = (len(after_pairs) > 0)

            if mode == "strict":
                if after_collide_fast != desired_after_collide:
                    continue
                if before_collide_fast and not after_collide_fast:
                    involved_before = any(a_id in pair or b_id in pair for pair in before_pairs)
                    if not involved_before:
                        continue
                elif not before_collide_fast and after_collide_fast:
                    involved_after = any(a_id in pair or b_id in pair for pair in after_pairs)
                    if not involved_after:
                        continue
            elif mode == "flip":
                if after_collide_fast == before_collide_fast:
                    continue
            else:
                continue

            after_pairs_exact = _exact_scene_collision_pairs(pos_overrides=pos_overrides)
            after_collide_exact = (len(after_pairs_exact) > 0)

            # Final exact check before persisting answer; mismatch -> keep searching.
            if after_collide_exact != after_collide_fast:
                continue

            if mode == "strict":
                if after_collide_exact != desired_after_collide:
                    continue
                if before_collide and not after_collide_exact:
                    involved_before_exact = any(a_id in pair or b_id in pair for pair in before_pairs_exact)
                    if not involved_before_exact:
                        continue
                elif not before_collide and after_collide_exact:
                    involved_after_exact = any(a_id in pair or b_id in pair for pair in after_pairs_exact)
                    if not involved_after_exact:
                        continue
            elif mode == "flip":
                if after_collide_exact == before_collide:
                    continue
            else:
                continue

            task = qa_task_swap_collision_yesno(sample_dir, a_id, b_id, before_collide, after_collide_exact)
            return task, (a_id, b_id)
        return None, None

    swap_task, swap_ref = _pick_swap("strict")
    if swap_task is None:
        swap_task, swap_ref = _pick_swap("flip")

    # If both failed, skip region
    if move_task is None and swap_task is None:
        print(f"[Region {region_id}] failed: no tasks generated")
        labeler.clear_all_labels()
        labeler.clear_mask_materials()
        labeler.show_all_visible_objects()
        return None

    # ----------------------------------------------------------
    # Render top_swap.png (north panel includes 1-unit scale bar)
    # ----------------------------------------------------------
    render_ref_top(
        cam=cam_top,
        output_path=os.path.join(sample_dir, "top_swap.png"),
        bounds=bounds,
        building_objs=objs,
        id_to_obj=id_to_obj,
        with_scale_marker=False,
        label_font_size=label_font_size,
        region_max_dim=region_max_dim,
    )

    # Render top_move.png (north panel includes 1-unit scale bar)
    render_ref_top(
        cam=cam_top,
        output_path=os.path.join(sample_dir, "top_move.png"),
        bounds=bounds,
        building_objs=objs,
        id_to_obj=id_to_obj,
        with_scale_marker=True,
        label_font_size=label_font_size,
        region_max_dim=region_max_dim,
    )

    # ----------------------------------------------------------
    # Render QA1 move after (north panel includes 1-unit scale bar)
    # ----------------------------------------------------------
    if move_ref is not None:
        m_id, nx, ny = move_ref
        obj_m = id_to_obj[m_id]
        old_loc = obj_m.location.copy()
        # Get the OBB center from meta for correct delta calculation
        meta_m = metas[m_id - 1]
        obb_center_x, obb_center_y = meta_m["obb"]["center"][:2]
        # Calculate delta: where we want OBB to be minus where it currently is
        delta_x = float(nx - obb_center_x)
        delta_y = float(ny - obb_center_y)
        try:
            # Apply delta to object's actual location
            obj_m.location = (float(old_loc.x + delta_x), float(old_loc.y + delta_y), float(old_loc.z))
            bpy.context.view_layer.update()

            # Recompute bounds based on moved object positions
            new_bounds = compute_scene_bounds(objs)
            new_region_max_dim = max(new_bounds["width"], new_bounds["depth"])
            new_label_font_size = float(new_region_max_dim) * LABEL_SIZE_RATIO

            render_ref_top(
                cam=cam_top,
                output_path=os.path.join(ref_dir, "qa1_move_after.png"),
                bounds=new_bounds,
                building_objs=objs,
                id_to_obj=id_to_obj,
                with_scale_marker=True,
                label_font_size=new_label_font_size,
                region_max_dim=new_region_max_dim,
            )
        finally:
            obj_m.location = old_loc
            bpy.context.view_layer.update()

    # ----------------------------------------------------------
    # Render QA2 swap after (north panel includes 1-unit scale bar)
    # ----------------------------------------------------------
    if swap_ref is not None:
        a_id, b_id = swap_ref
        obj_a = id_to_obj[a_id]
        obj_b = id_to_obj[b_id]
        loc_a = obj_a.location.copy()
        loc_b = obj_b.location.copy()

        # Get OBB centers for correct delta calculation
        meta_a = metas[a_id - 1]
        meta_b = metas[b_id - 1]
        obb_a = meta_a["obb"]["center"][:2]
        obb_b = meta_b["obb"]["center"][:2]

        # Calculate deltas: where we want each OBB to be
        # For swap: a should go to b's position, b should go to a's position
        delta_a_x = obb_b[0] - obb_a[0]  # b's pos - a's pos
        delta_a_y = obb_b[1] - obb_a[1]
        delta_b_x = obb_a[0] - obb_b[0]  # a's pos - b's pos
        delta_b_y = obb_a[1] - obb_b[1]

        try:
            # Apply deltas to object locations
            obj_a.location = (float(loc_a.x + delta_a_x), float(loc_a.y + delta_a_y), float(loc_a.z))
            obj_b.location = (float(loc_b.x + delta_b_x), float(loc_b.y + delta_b_y), float(loc_b.z))
            bpy.context.view_layer.update()

            # Recompute bounds based on swapped object positions
            new_bounds = compute_scene_bounds(objs)
            new_region_max_dim = max(new_bounds["width"], new_bounds["depth"])
            new_label_font_size = float(new_region_max_dim) * LABEL_SIZE_RATIO

            render_ref_top(
                cam=cam_top,
                output_path=os.path.join(ref_dir, "qa2_swap_after.png"),
                bounds=new_bounds,
                building_objs=objs,
                id_to_obj=id_to_obj,
                with_scale_marker=False,  # legacy arg; scale bar is in north panel
                label_font_size=new_label_font_size,
                region_max_dim=new_region_max_dim,
            )
        finally:
            obj_a.location = loc_a
            obj_b.location = loc_b
            bpy.context.view_layer.update()

    # Cleanup visibility
    labeler.clear_all_labels()
    labeler.clear_mask_materials()
    labeler.show_all_visible_objects()

    # Collect images
    images = {
        "top_swap": os.path.abspath(os.path.join(sample_dir, "top_swap.png")),
        "top_move": os.path.abspath(os.path.join(sample_dir, "top_move.png")),
    }
    if move_ref is not None:
        images["qa1_move_after"] = os.path.abspath(os.path.join(ref_dir, "qa1_move_after.png"))
    if swap_ref is not None:
        images["qa2_swap_after"] = os.path.abspath(os.path.join(ref_dir, "qa2_swap_after.png"))

    qa_tasks = [t for t in [move_task, swap_task] if t is not None]

    return {
        "region_id": int(region_id),
        "sample_index": int(sample_idx),
        "attempt": int(max(0, variant_idx - 1)),
        "variant_index": int(variant_idx),
        "variant_total": int(variant_total),
        "bounds": bounds,
        "building_count": len(objs),
        "buildings": metas,  # metas include obb
        "images": images,
        "qa": {"top": qa_tasks},
        "special_refs": {
            "scale_marker_units": SCALE_MARK_LENGTH_UNITS,
            "scale_marker_in_north_panel": True,
            "qa1_move_adjacent_pair": True,
            "qa1_balanced_collide": desired_move_collide,
            "qa2_state_flip": True,
            "qa2_desired_before": before_collide,
            "qa2_desired_after": desired_after_collide,
            "qa2_collision_mode": "AABB/OBB_candidate_search+exact_mesh_projection_final_check",
        },
        "camera_params": {
            "top": {"ortho_scale": cam_top.data.ortho_scale, "location": tuple(cam_top.location)},
        },
    }


# --------------------------------------------------------------------------------------
# Main
# --------------------------------------------------------------------------------------
def main():
    use_clean = USE_CLEAN_ARG == 1
    if "USE_CLEAN_SCENE" in os.environ:
        use_clean = os.environ.get("USE_CLEAN_SCENE", "1").lower() in ("1", "true", "yes")

    effective_blend_path, effective_region_json_path, failed_region_ids, using_clean = resolve_region_inputs(
        BLEND_PATH,
        REGION_DIR,
        use_clean=use_clean,
    )

    print("=" * 60)
    print(f"[RUN] region={REGION_NAME}")
    print(f"[BLEND] {effective_blend_path}")
    print(f"[REGION_DIR] {REGION_DIR}")
    print(f"[REGION_JSON] {effective_region_json_path}")
    print(f"[USING_CLEAN] {using_clean}")
    print(f"[OUT] {OUTPUT_DIR}")
    print("=" * 60)
    os.makedirs(OUTPUT_DIR, exist_ok=True)

    # Load region json
    region_data = load_region_data(effective_region_json_path)
    regions = region_data.get("regions", [])
    raw_region_count = len(regions)
    if failed_region_ids:
        regions = [r for r in regions if int(r.get("region_id", -1)) not in failed_region_ids]
        print(f"[INFO] after failed-region filter: {len(regions)}/{raw_region_count}")
    if MAX_REGIONS > 0:
        regions = regions[:MAX_REGIONS]
    print(f"[INFO] regions={len(regions)}")

    # Open blend once
    bpy.ops.wm.open_mainfile(filepath=effective_blend_path)
    render_resolution = (RESOLUTION, RESOLUTION) if RESOLUTION > 0 else _render_resolution_px()

    # Render settings
    setup_render(render_resolution)
    force_disable_all_shadows_and_world()

    # Labeler for camera + masking
    labeler = BuildingLabeler(
        region_data_path=effective_region_json_path,
        blend_path=effective_blend_path,
        output_dir=OUTPUT_DIR,
        ortho_scale_factor=1.1,  # Match blender_apply_final_all_regions.py
        label_height_ratio=0.01,
        font_size_ratio=0.07,
        samples=32,
        resolution=render_resolution,
        mask_alpha=0.8,
    )
    # patch lighting to no-shadow
    def _patched_setup_lighting(self):
        scene = bpy.context.scene
        for obj in list(bpy.data.objects):
            if obj.type == "LIGHT":
                bpy.data.objects.remove(obj)

        sun = bpy.data.objects.new("Sun", bpy.data.lights.new("SunLight", type="SUN"))
        bpy.context.collection.objects.link(sun)
        sun.data.energy = 4.0
        sun.rotation_euler = (math.radians(45), math.radians(15), math.radians(30))
        if hasattr(sun.data, "use_shadow"):
            sun.data.use_shadow = False

        fill = bpy.data.objects.new("FillLight", bpy.data.lights.new("FillLight", type="AREA"))
        bpy.context.collection.objects.link(fill)
        fill.data.energy = 150.0
        fill.location = (0.0, 0.0, 100.0)
        fill.data.size = 100.0
        if hasattr(fill.data, "use_shadow"):
            fill.data.use_shadow = False

        scene.cycles.use_shadows = False
        scene.cycles.blur_shadow = 0
        for o in bpy.data.objects:
            if hasattr(o, "cycles_visibility"):
                o.cycles_visibility.cast_shadow = False
                o.cycles_visibility.receive_shadow = False

    labeler.setup_lighting = _patched_setup_lighting.__get__(labeler, BuildingLabeler)
    labeler.setup_render()
    labeler.setup_lighting()

    all_results = []
    ok = 0
    for idx, r in enumerate(regions):
        rid = r.get("region_id", idx)
        print(f"\n[PROCESS] region_id={rid} idx={idx}")
        region_ok = 0
        for variant_idx in range(1, int(QA_PAIRS_PER_REGION) + 1):
            global_sample_idx = idx * int(QA_PAIRS_PER_REGION) + (variant_idx - 1)
            res = process_region(
                rid,
                r,
                labeler,
                global_sample_idx,
                variant_idx=variant_idx,
                variant_total=int(QA_PAIRS_PER_REGION),
            )
            if res is not None:
                all_results.append(res)
                ok += 1
                region_ok += 1
        if int(QA_PAIRS_PER_REGION) > 1:
            print(f"[PROCESS] region_id={rid}: variants ok={region_ok}/{int(QA_PAIRS_PER_REGION)}")

    meta_path = os.path.join(OUTPUT_DIR, "metadata_regions.json")
    with open(meta_path, "w", encoding="utf-8") as f:
        json.dump(all_results, f, ensure_ascii=False, indent=2)

    print("\nDONE")
    total_attempts = len(regions) * int(QA_PAIRS_PER_REGION)
    print(f"  ok={ok}/{total_attempts} (variants_per_region={int(QA_PAIRS_PER_REGION)})")
    print(f"  out={OUTPUT_DIR}")
    print(f"  meta={meta_path}")


if __name__ == "__main__":
    main()
