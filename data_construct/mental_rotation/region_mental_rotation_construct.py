def _object_world_xy_bounds(obj) -> tuple[float, float, float, float] | None:
    if obj is None or obj.type != "MESH":
        return None
    if hasattr(obj, "bound_box") and obj.bound_box:
        verts = [obj.matrix_world @ Vector(co) for co in obj.bound_box]
    else:
        verts = [obj.matrix_world @ v.co for v in obj.data.vertices]
    if not verts:
        return None
    xs = [float(v.x) for v in verts]
    ys = [float(v.y) for v in verts]
    return (min(xs), max(xs), min(ys), max(ys))


def compute_pca_obb_xy(world_xy_pts: list[tuple[float, float]]) -> dict | None:
    if not world_xy_pts:
        return None
    n = len(world_xy_pts)
    mx = sum(p[0] for p in world_xy_pts) / n
    my = sum(p[1] for p in world_xy_pts) / n
    xx = sum((p[0] - mx) ** 2 for p in world_xy_pts)
    yy = sum((p[1] - my) ** 2 for p in world_xy_pts)
    xy = sum((p[0] - mx) * (p[1] - my) for p in world_xy_pts)

    if abs(xy) < 1e-9:
        angle = 0.0 if xx >= yy else (math.pi / 2.0)
    else:
        angle = 0.5 * math.atan2(2.0 * xy, xx - yy)

    ca = math.cos(angle)
    sa = math.sin(angle)
    ax = (ca, sa)
    ay = (-sa, ca)

    rot = []
    for (x, y) in world_xy_pts:
        dx = x - mx
        dy = y - my
        rx = ca * dx + sa * dy
        ry = -sa * dx + ca * dy
        rot.append((rx, ry))

    rxs = [p[0] for p in rot]
    rys = [p[1] for p in rot]
    min_rx, max_rx = min(rxs), max(rxs)
    min_ry, max_ry = min(rys), max(rys)

    hx = 0.5 * (max_rx - min_rx)
    hy = 0.5 * (max_ry - min_ry)
    crx = 0.5 * (min_rx + max_rx)
    cry = 0.5 * (min_ry + max_ry)
    cx = mx + ca * crx - sa * cry
    cy = my + sa * crx + ca * cry

    return {
        "angle": float(angle),
        "ax": (float(ax[0]), float(ax[1])),
        "ay": (float(ay[0]), float(ay[1])),
        "hx": float(hx),
        "hy": float(hy),
        "center": (float(cx), float(cy)),
    }


def build_collision_meta_from_obj(obj: bpy.types.Object) -> dict | None:
    b = _object_world_xy_bounds(obj)
    if b is None:
        return None
    min_x, max_x, min_y, max_y = b
    half_w = 0.5 * float(max_x - min_x)
    half_d = 0.5 * float(max_y - min_y)

    verts = obj.data.vertices if obj and obj.type == "MESH" and obj.data else []
    pts = []
    step = max(1, len(verts) // 500) if verts else 1
    for i in range(0, len(verts), step):
        w = obj.matrix_world @ verts[i].co
        pts.append((float(w.x), float(w.y)))

    obb = compute_pca_obb_xy(pts)
    if obb is None:
        obb = {
            "angle": 0.0,
            "ax": (1.0, 0.0),
            "ay": (0.0, 1.0),
            "hx": half_w,
            "hy": half_d,
            "center": (float((min_x + max_x) * 0.5), float((min_y + max_y) * 0.5)),
        }

    return {
        "half_w": float(half_w),
        "half_d": float(half_d),
        "obb": obb,
    }


def aabb_overlap_xy_by_meta(ma: dict, mb: dict, inflate: float = 0.0) -> bool:
    ax, ay = tuple(ma["obb"]["center"][:2])
    bx, by = tuple(mb["obb"]["center"][:2])
    inf = float(inflate)

    min_ax = ax - ma["half_w"] - inf
    max_ax = ax + ma["half_w"] + inf
    min_ay = ay - ma["half_d"] - inf
    max_ay = ay + ma["half_d"] + inf

    min_bx = bx - mb["half_w"] - inf
    max_bx = bx + mb["half_w"] + inf
    min_by = by - mb["half_d"] - inf
    max_by = by + mb["half_d"] + inf

    if max_ax < min_bx or min_ax > max_bx:
        return False
    if max_ay < min_by or min_ay > max_by:
        return False
    return True


def _normalize2(v: tuple[float, float]) -> tuple[float, float]:
    l = math.hypot(v[0], v[1])
    if l < 1e-9:
        return (1.0, 0.0)
    return (v[0] / l, v[1] / l)


def _project_obb(center, ax, ay, hx, hy, axis):
    dots = []
    for sx in (-1.0, 1.0):
        for sy in (-1.0, 1.0):
            px = center[0] + ax[0] * (sx * hx) + ay[0] * (sy * hy)
            py = center[1] + ax[1] * (sx * hx) + ay[1] * (sy * hy)
            dots.append(px * axis[0] + py * axis[1])
    return min(dots), max(dots)


def obb_overlap(ma: dict, mb: dict, inflate: float = 0.0) -> bool:
    oa = ma["obb"]
    ob = mb["obb"]

    ca = tuple(oa["center"][:2])
    cb = tuple(ob["center"][:2])
    ax_a, ay_a = oa["ax"], oa["ay"]
    ax_b, ay_b = ob["ax"], ob["ay"]

    hx_a = float(oa["hx"] + inflate)
    hy_a = float(oa["hy"] + inflate)
    hx_b = float(ob["hx"] + inflate)
    hy_b = float(ob["hy"] + inflate)

    axes = [ax_a, ay_a, ax_b, ay_b]
    for axis in axes:
        axis = _normalize2(axis)
        a0, a1 = _project_obb(ca, ax_a, ay_a, hx_a, hy_a, axis)
        b0, b1 = _project_obb(cb, ax_b, ay_b, hx_b, hy_b, axis)
        if a1 < b0 or b1 < a0:
            return False
    return True


def _tri_area2_xy(a: tuple[float, float], b: tuple[float, float], c: tuple[float, float]) -> float:
    return float((b[0] - a[0]) * (c[1] - a[1]) - (b[1] - a[1]) * (c[0] - a[0]))


def _tri_bbox_xy(tri: tuple[tuple[float, float], tuple[float, float], tuple[float, float]]) -> tuple[float, float, float, float]:
    xs = (tri[0][0], tri[1][0], tri[2][0])
    ys = (tri[0][1], tri[1][1], tri[2][1])
    return (min(xs), max(xs), min(ys), max(ys))


def _point_in_tri_xy(p: tuple[float, float], tri: tuple[tuple[float, float], tuple[float, float], tuple[float, float]], eps: float = 1e-9) -> bool:
    a, b, c = tri
    s1 = _tri_area2_xy(a, b, p)
    s2 = _tri_area2_xy(b, c, p)
    s3 = _tri_area2_xy(c, a, p)
    has_neg = (s1 < -eps) or (s2 < -eps) or (s3 < -eps)
    has_pos = (s1 > eps) or (s2 > eps) or (s3 > eps)
    return not (has_neg and has_pos)


def _on_segment_xy(a: tuple[float, float], b: tuple[float, float], p: tuple[float, float], eps: float = 1e-9) -> bool:
    return (
        min(a[0], b[0]) - eps <= p[0] <= max(a[0], b[0]) + eps
        and min(a[1], b[1]) - eps <= p[1] <= max(a[1], b[1]) + eps
    )


def _segments_intersect_xy(a1, a2, b1, b2, eps: float = 1e-9) -> bool:
    o1 = _tri_area2_xy(a1, a2, b1)
    o2 = _tri_area2_xy(a1, a2, b2)
    o3 = _tri_area2_xy(b1, b2, a1)
    o4 = _tri_area2_xy(b1, b2, a2)

    if ((o1 > eps and o2 < -eps) or (o1 < -eps and o2 > eps)) and ((o3 > eps and o4 < -eps) or (o3 < -eps and o4 > eps)):
        return True

    if abs(o1) <= eps and _on_segment_xy(a1, a2, b1, eps):
        return True
    if abs(o2) <= eps and _on_segment_xy(a1, a2, b2, eps):
        return True
    if abs(o3) <= eps and _on_segment_xy(b1, b2, a1, eps):
        return True
    if abs(o4) <= eps and _on_segment_xy(b1, b2, a2, eps):
        return True

    return False


def _triangles_overlap_xy(
    ta: tuple[tuple[float, float], tuple[float, float], tuple[float, float]],
    tb: tuple[tuple[float, float], tuple[float, float], tuple[float, float]],
    eps: float = 1e-9,
) -> bool:
    a_min_x, a_max_x, a_min_y, a_max_y = _tri_bbox_xy(ta)
    b_min_x, b_max_x, b_min_y, b_max_y = _tri_bbox_xy(tb)
    if a_max_x < b_min_x - eps or b_max_x < a_min_x - eps or a_max_y < b_min_y - eps or b_max_y < a_min_y - eps:
        return False

    ea = ((ta[0], ta[1]), (ta[1], ta[2]), (ta[2], ta[0]))
    eb = ((tb[0], tb[1]), (tb[1], tb[2]), (tb[2], tb[0]))
    for sa0, sa1 in ea:
        for sb0, sb1 in eb:
            if _segments_intersect_xy(sa0, sa1, sb0, sb1, eps=eps):
                return True

    if _point_in_tri_xy(ta[0], tb, eps=eps):
        return True
    if _point_in_tri_xy(tb[0], ta, eps=eps):
        return True
    return False


def _object_world_xy_triangles(obj: bpy.types.Object, min_area: float = 1e-10) -> list[tuple[tuple[float, float], tuple[float, float], tuple[float, float]]]:
    if obj is None or obj.type != "MESH" or obj.data is None:
        return []
    mw = obj.matrix_world
    tris: list[tuple[tuple[float, float], tuple[float, float], tuple[float, float]]] = []
    verts = obj.data.vertices
    for poly in obj.data.polygons:
        idx = list(poly.vertices)
        if len(idx) < 3:
            continue
        p0w = mw @ verts[idx[0]].co
        p0 = (float(p0w.x), float(p0w.y))
        for k in range(1, len(idx) - 1):
            p1w = mw @ verts[idx[k]].co
            p2w = mw @ verts[idx[k + 1]].co
            p1 = (float(p1w.x), float(p1w.y))
            p2 = (float(p2w.x), float(p2w.y))
            if abs(_tri_area2_xy(p0, p1, p2)) > min_area:
                tris.append((p0, p1, p2))
    return tris


def exact_mesh_overlap_xy(obj_a: bpy.types.Object, obj_b: bpy.types.Object) -> bool:
    ta = _object_world_xy_triangles(obj_a)
    tb = _object_world_xy_triangles(obj_b)
    if not ta or not tb:
        return False

    bboxes_a = [_tri_bbox_xy(t) for t in ta]
    bboxes_b = [_tri_bbox_xy(t) for t in tb]

    for i, tri_a in enumerate(ta):
        a_min_x, a_max_x, a_min_y, a_max_y = bboxes_a[i]
        for j, tri_b in enumerate(tb):
            b_min_x, b_max_x, b_min_y, b_max_y = bboxes_b[j]
            if a_max_x < b_min_x or b_max_x < a_min_x or a_max_y < b_min_y or b_max_y < a_min_y:
                continue
            if _triangles_overlap_xy(tri_a, tri_b):
                return True
    return False


def relation_2d_with_pad(target_obj, target_meta, other_obj, other_meta, pad: float) -> str:
    _ = target_meta
    _ = other_meta
    ma = build_collision_meta_from_obj(target_obj)
    mb = build_collision_meta_from_obj(other_obj)
    if ma is None or mb is None:
        return "SAFE"

    pad = float(pad)
    if not aabb_overlap_xy_by_meta(ma, mb, inflate=pad):
        return "SAFE"
    # COLLIDE: use exact projected mesh overlap in XY (avoids OBB false positives on concave footprints).
    if exact_mesh_overlap_xy(target_obj, other_obj):
        return "COLLIDE"
    if obb_overlap(ma, mb, inflate=pad):
        return "NEAR"
    return "SAFE"


def _set_building_rotation_deg(obj, deg: float) -> None:
    obj.rotation_euler[2] = math.radians(deg)
    bpy.context.view_layer.update()


def _reset_building_rotation(obj) -> None:
    obj.rotation_euler[2] = 0.0
    bpy.context.view_layer.update()


def _capture_obj_pose(obj) -> tuple[Vector, tuple[float, float, float]]:
    return obj.location.copy(), tuple(float(v) for v in obj.rotation_euler)


def _restore_obj_pose(obj, pose: tuple[Vector, tuple[float, float, float]]) -> None:
    loc, rot = pose
    obj.location = loc
    obj.rotation_euler = rot
    bpy.context.view_layer.update()


def _object_bbox_center_xy(obj) -> Vector:
    b = _object_world_xy_bounds(obj)
    if b is None:
        return Vector((float(obj.location.x), float(obj.location.y), float(obj.location.z)))
    min_x, max_x, min_y, max_y = b
    return Vector((float((min_x + max_x) * 0.5), float((min_y + max_y) * 0.5), float(obj.location.z)))


def _set_building_rotation_deg_about_bbox_center(obj, deg: float) -> None:
    """
    Rotate object by `deg` (relative) around its current world-space bbox center.
    """
    center = _object_bbox_center_xy(obj)
    loc = obj.location.copy()
    ang = math.radians(float(deg))
    rel_x = float(loc.x - center.x)
    rel_y = float(loc.y - center.y)
    cos_a = math.cos(ang)
    sin_a = math.sin(ang)
    new_x = float(center.x + rel_x * cos_a - rel_y * sin_a)
    new_y = float(center.y + rel_x * sin_a + rel_y * cos_a)
    obj.location = (new_x, new_y, float(loc.z))
    obj.rotation_euler[2] = float(obj.rotation_euler[2]) + ang
    bpy.context.view_layer.update()


def _rotate_dir_to_signed_deg(direction: str, deg: int) -> int:
    return -deg if direction == "cw" else deg


def _dir_token_to_words(direction: str) -> str:
    return "clockwise" if direction == "cw" else "counterclockwise"


def _rotate_building_around_point(obj, point: Vector, angle_deg: float) -> None:
    """Rotate a building around a specified point by the specified angle

    Args:
        obj: Blender building object
        point: rotation center point (Vector)
        angle_deg: rotation angle in degrees
    """
    # Get current object position
    obj_loc = obj.location

    # Compute vector relative to rotation center
    rel_vec = obj_loc - point

    # Rotate around Z axis
    angle_rad = math.radians(angle_deg)
    cos_a = math.cos(angle_rad)
    sin_a = math.sin(angle_rad)

    # New position after rotation
    new_x = point.x + rel_vec.x * cos_a - rel_vec.y * sin_a
    new_y = point.y + rel_vec.x * sin_a + rel_vec.y * cos_a

    obj.location = (new_x, new_y, obj_loc.z)
    obj.rotation_euler[2] = math.radians(angle_deg)
    bpy.context.view_layer.update()


def _reset_building_rotation_and_position(obj, original_loc: Vector) -> None:
    """Reset building position and rotation."""
    obj.location = original_loc
    obj.rotation_euler[2] = 0.0
    bpy.context.view_layer.update()


def _relation_after_rotation(target_obj, target_meta, other_obj, other_meta, signed_deg: float, pad: float) -> str:
    pose = _capture_obj_pose(target_obj)
    try:
        _set_building_rotation_deg_about_bbox_center(target_obj, signed_deg)
        return relation_2d_with_pad(target_obj, target_meta, other_obj, other_meta, pad=pad)
    finally:
        _restore_obj_pose(target_obj, pose)


