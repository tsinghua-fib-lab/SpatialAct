# --------------------------------------------------------------------------------------
# Move-effect auto-detect + bake (critical for move/swap after rendering correctness)
# --------------------------------------------------------------------------------------
def does_location_move_geometry(obj: bpy.types.Object, delta_x: float = UNIT_SCALE) -> bool:
    """Test if changing obj.location changes its world bounds center."""
    b0 = world_bounds_from_obj(obj)
    old_loc = obj.location.copy()
    obj.location.x += float(delta_x)
    bpy.context.view_layer.update()
    b1 = world_bounds_from_obj(obj)
    obj.location = old_loc
    bpy.context.view_layer.update()
    dx = abs(b1["center_x"] - b0["center_x"])
    return dx > (abs(delta_x) * 0.6)


def bake_mesh_to_local_and_reset_transform(obj: bpy.types.Object) -> None:
    """
    Bake current world transform into mesh vertex positions, then reset matrix_world to identity.
    After this, obj.location will reliably move the geometry.
    """
    if obj.type != "MESH" or obj.data is None:
        return

    mw = obj.matrix_world.copy()

    # Write world coords into local vertex coords
    mesh = obj.data
    for v in mesh.vertices:
        v.co = mw @ v.co
    mesh.update()

    # Reset transform
    obj.matrix_world = Matrix.Identity(4)
    bpy.context.view_layer.update()


def ensure_buildings_movable(building_objs: list[bpy.types.Object]) -> None:
    """
    If location does not move geometry, bake all buildings once.
    """
    if not building_objs:
        return
    test = building_objs[0]
    if does_location_move_geometry(test):
        print("[INFO] location affects geometry -> no bake needed")
        return

    print("[WARN] location does NOT affect geometry -> baking buildings to local space...")
    for o in building_objs:
        bake_mesh_to_local_and_reset_transform(o)

    bpy.context.view_layer.update()
    # Re-test
    if not does_location_move_geometry(building_objs[0]):
        print("[ERROR] bake did not make buildings movable; move/swap after render may still be wrong.")


# --------------------------------------------------------------------------------------
# PCA OBB footprint computation (2D)
# --------------------------------------------------------------------------------------
def compute_pca_obb_xy(world_xy_pts: list[tuple[float, float]]) -> dict | None:
    """
    Given 2D points in world XY, compute an oriented bounding box using PCA.
    Return:
      angle (radians), ax, ay, hx, hy, center (cx,cy)
    """
    if not world_xy_pts:
        return None
    n = len(world_xy_pts)
    mx = sum(p[0] for p in world_xy_pts) / n
    my = sum(p[1] for p in world_xy_pts) / n
    xx = sum((p[0] - mx) ** 2 for p in world_xy_pts)
    yy = sum((p[1] - my) ** 2 for p in world_xy_pts)
    xy = sum((p[0] - mx) * (p[1] - my) for p in world_xy_pts)

    if abs(xy) < 1e-9:
        angle = 0.0 if xx >= yy else (math.pi / 2.0)
    else:
        angle = 0.5 * math.atan2(2.0 * xy, xx - yy)

    ca = math.cos(angle)
    sa = math.sin(angle)
    ax = (ca, sa)
    ay = (-sa, ca)

    # Rotate points into OBB frame
    rot = []
    for (x, y) in world_xy_pts:
        dx = x - mx
        dy = y - my
        rx = ca * dx + sa * dy
        ry = -sa * dx + ca * dy
        rot.append((rx, ry))

    rxs = [p[0] for p in rot]
    rys = [p[1] for p in rot]
    min_rx, max_rx = min(rxs), max(rxs)
    min_ry, max_ry = min(rys), max(rys)

    hx = 0.5 * (max_rx - min_rx)
    hy = 0.5 * (max_ry - min_ry)
    # OBB center in rotated frame
    crx = 0.5 * (min_rx + max_rx)
    cry = 0.5 * (min_ry + max_ry)
    # Convert center back to world
    cx = mx + ca * crx - sa * cry
    cy = my + sa * crx + ca * cry

    return {
        "angle": float(angle),
        "ax": (float(ax[0]), float(ax[1])),
        "ay": (float(ay[0]), float(ay[1])),
        "hx": float(hx),
        "hy": float(hy),
        "center": (float(cx), float(cy)),
    }


def build_meta_from_obj(obj: bpy.types.Object, label_id: int) -> dict:
    """
    Build collision/render meta from the current object:
    - pos: obj.location xy (consistent with rendering-time movement)
    - half_w/half_d: world AABB half extents
    - obb: PCA OBB on footprint sampled from vertices projected to XY
    """
    b = world_bounds_from_obj(obj)

    # Key point: use obj.location as pos so it stays consistent with obj.location movement during rendering
    pos_x = float(obj.location.x)
    pos_y = float(obj.location.y)

    # Sample footprint points: project vertices to XY.
    # To keep it fast, sample up to N vertices.
    mw = obj.matrix_world
    verts = obj.data.vertices
    pts = []
    step = max(1, len(verts) // 500)  # cap ~500 samples
    for i in range(0, len(verts), step):
        w = mw @ verts[i].co
        pts.append((float(w.x), float(w.y)))

    obb = compute_pca_obb_xy(pts)  # None if no pts
    if obb is None:
        obb = {
            "angle": 0.0,
            "ax": (1.0, 0.0),
            "ay": (0.0, 1.0),
            "hx": b["half_w"],
            "hy": b["half_d"],
            "center": (pos_x, pos_y),
        }

    return {
        "label_id": int(label_id),
        "original_id": obj.name,
        "pos": (pos_x, pos_y),
        "half_w": float(b["half_w"]),
        "half_d": float(b["half_d"]),
        "height": float(b["height"]),
        "obb": obb,  # world-frame OBB
    }


# --------------------------------------------------------------------------------------
# Collision: AABB precheck + OBB overlap (SAT)
# --------------------------------------------------------------------------------------
def aabb_overlap_xy_by_meta(ma: dict, mb: dict, inflate: float = 0.0, pos_a=None, pos_b=None) -> bool:
    # Use obb center if no pos override provided
    ax, ay = pos_a if pos_a is not None else tuple(ma["obb"]["center"][:2])
    bx, by = pos_b if pos_b is not None else tuple(mb["obb"]["center"][:2])
    inf = float(inflate)
    # Use half_w/half_d (axis-aligned AABB) for quick precheck
    # This is more accurate than using rotated OBB hx/hy
    min_ax = ax - ma["half_w"] - inf
    max_ax = ax + ma["half_w"] + inf
    min_ay = ay - ma["half_d"] - inf
    max_ay = ay + ma["half_d"] + inf

    min_bx = bx - mb["half_w"] - inf
    max_bx = bx + mb["half_w"] + inf
    min_by = by - mb["half_d"] - inf
    max_by = by + mb["half_d"] + inf

    if max_ax < min_bx or min_ax > max_bx:
        return False
    if max_ay < min_by or min_ay > max_by:
        return False
    return True


def _normalize2(v: tuple[float, float]) -> tuple[float, float]:
    l = math.hypot(v[0], v[1])
    if l < 1e-9:
        return (1.0, 0.0)
    return (v[0] / l, v[1] / l)


def _project_obb(center, ax, ay, hx, hy, axis):
    dots = []
    for sx in (-1.0, 1.0):
        for sy in (-1.0, 1.0):
            px = center[0] + ax[0] * (sx * hx) + ay[0] * (sy * hy)
            py = center[1] + ax[1] * (sx * hx) + ay[1] * (sy * hy)
            dots.append(px * axis[0] + py * axis[1])
    return min(dots), max(dots)


def obb_overlap(ma: dict, mb: dict, inflate: float = 0.0, pos_a=None, pos_b=None) -> bool:
    oa = ma["obb"]
    ob = mb["obb"]

    # Use obb center if no pos override, otherwise use pos directly as new center
    ca = tuple(pos_a[:2]) if pos_a is not None else tuple(oa["center"][:2])
    cb = tuple(pos_b[:2]) if pos_b is not None else tuple(ob["center"][:2])

    ax_a, ay_a = oa["ax"], oa["ay"]
    ax_b, ay_b = ob["ax"], ob["ay"]

    hx_a = float(oa["hx"] + inflate)
    hy_a = float(oa["hy"] + inflate)
    hx_b = float(ob["hx"] + inflate)
    hy_b = float(ob["hy"] + inflate)

    axes = [ax_a, ay_a, ax_b, ay_b]
    for axis in axes:
        axis = _normalize2(axis)
        a0, a1 = _project_obb(ca, ax_a, ay_a, hx_a, hy_a, axis)
        b0, b1 = _project_obb(cb, ax_b, ay_b, hx_b, hy_b, axis)
        if a1 < b0 or b1 < a0:
            return False
    return True


def _tri_area2_xy(a: tuple[float, float], b: tuple[float, float], c: tuple[float, float]) -> float:
    return float((b[0] - a[0]) * (c[1] - a[1]) - (b[1] - a[1]) * (c[0] - a[0]))


def _tri_bbox_xy(tri: tuple[tuple[float, float], tuple[float, float], tuple[float, float]]) -> tuple[float, float, float, float]:
    xs = (tri[0][0], tri[1][0], tri[2][0])
    ys = (tri[0][1], tri[1][1], tri[2][1])
    return (min(xs), max(xs), min(ys), max(ys))


def _point_in_tri_xy(
    p: tuple[float, float],
    tri: tuple[tuple[float, float], tuple[float, float], tuple[float, float]],
    eps: float = 1e-9,
) -> bool:
    a, b, c = tri
    s1 = _tri_area2_xy(a, b, p)
    s2 = _tri_area2_xy(b, c, p)
    s3 = _tri_area2_xy(c, a, p)
    has_neg = (s1 < -eps) or (s2 < -eps) or (s3 < -eps)
    has_pos = (s1 > eps) or (s2 > eps) or (s3 > eps)
    return not (has_neg and has_pos)


def _on_segment_xy(a: tuple[float, float], b: tuple[float, float], p: tuple[float, float], eps: float = 1e-9) -> bool:
    return (
        min(a[0], b[0]) - eps <= p[0] <= max(a[0], b[0]) + eps
        and min(a[1], b[1]) - eps <= p[1] <= max(a[1], b[1]) + eps
    )


def _segments_intersect_xy(a1, a2, b1, b2, eps: float = 1e-9) -> bool:
    o1 = _tri_area2_xy(a1, a2, b1)
    o2 = _tri_area2_xy(a1, a2, b2)
    o3 = _tri_area2_xy(b1, b2, a1)
    o4 = _tri_area2_xy(b1, b2, a2)

    if ((o1 > eps and o2 < -eps) or (o1 < -eps and o2 > eps)) and ((o3 > eps and o4 < -eps) or (o3 < -eps and o4 > eps)):
        return True

    if abs(o1) <= eps and _on_segment_xy(a1, a2, b1, eps):
        return True
    if abs(o2) <= eps and _on_segment_xy(a1, a2, b2, eps):
        return True
    if abs(o3) <= eps and _on_segment_xy(b1, b2, a1, eps):
        return True
    if abs(o4) <= eps and _on_segment_xy(b1, b2, a2, eps):
        return True
    return False


def _triangles_overlap_xy(
    ta: tuple[tuple[float, float], tuple[float, float], tuple[float, float]],
    tb: tuple[tuple[float, float], tuple[float, float], tuple[float, float]],
    eps: float = 1e-9,
) -> bool:
    a_min_x, a_max_x, a_min_y, a_max_y = _tri_bbox_xy(ta)
    b_min_x, b_max_x, b_min_y, b_max_y = _tri_bbox_xy(tb)
    if a_max_x < b_min_x - eps or b_max_x < a_min_x - eps or a_max_y < b_min_y - eps or b_max_y < a_min_y - eps:
        return False

    ea = ((ta[0], ta[1]), (ta[1], ta[2]), (ta[2], ta[0]))
    eb = ((tb[0], tb[1]), (tb[1], tb[2]), (tb[2], tb[0]))
    for sa0, sa1 in ea:
        for sb0, sb1 in eb:
            if _segments_intersect_xy(sa0, sa1, sb0, sb1, eps=eps):
                return True

    if _point_in_tri_xy(ta[0], tb, eps=eps):
        return True
    if _point_in_tri_xy(tb[0], ta, eps=eps):
        return True
    return False


def _object_world_xy_triangles(
    obj: bpy.types.Object,
    min_area: float = 1e-10,
) -> list[tuple[tuple[float, float], tuple[float, float], tuple[float, float]]]:
    if obj is None or obj.type != "MESH" or obj.data is None:
        return []
    mw = obj.matrix_world
    tris: list[tuple[tuple[float, float], tuple[float, float], tuple[float, float]]] = []
    verts = obj.data.vertices
    for poly in obj.data.polygons:
        idx = list(poly.vertices)
        if len(idx) < 3:
            continue
        p0w = mw @ verts[idx[0]].co
        p0 = (float(p0w.x), float(p0w.y))
        for k in range(1, len(idx) - 1):
            p1w = mw @ verts[idx[k]].co
            p2w = mw @ verts[idx[k + 1]].co
            p1 = (float(p1w.x), float(p1w.y))
            p2 = (float(p2w.x), float(p2w.y))
            if abs(_tri_area2_xy(p0, p1, p2)) > min_area:
                tris.append((p0, p1, p2))
    return tris


def exact_mesh_overlap_xy(obj_a: bpy.types.Object, obj_b: bpy.types.Object) -> bool:
    ta = _object_world_xy_triangles(obj_a)
    tb = _object_world_xy_triangles(obj_b)
    if not ta or not tb:
        return False

    bboxes_a = [_tri_bbox_xy(t) for t in ta]
    bboxes_b = [_tri_bbox_xy(t) for t in tb]

    for i, tri_a in enumerate(ta):
        a_min_x, a_max_x, a_min_y, a_max_y = bboxes_a[i]
        for j, tri_b in enumerate(tb):
            b_min_x, b_max_x, b_min_y, b_max_y = bboxes_b[j]
            if a_max_x < b_min_x or b_max_x < a_min_x or a_max_y < b_min_y or b_max_y < a_min_y:
                continue
            if _triangles_overlap_xy(tri_a, tri_b):
                return True
    return False


def relation_2d_with_pad(meta_a: dict, meta_b: dict, pad: float, moved_pos=None) -> str:
    pad = float(pad)

    pos_a = moved_pos if moved_pos is not None else None

    # AABB precheck must use moved_pos
    if not aabb_overlap_xy_by_meta(meta_a, meta_b, inflate=pad, pos_a=pos_a, pos_b=None):
        return "SAFE"

    # Exact overlap: OBB
    if obb_overlap(meta_a, meta_b, inflate=0.0, pos_a=pos_a, pos_b=None):
        return "COLLIDE"
    if obb_overlap(meta_a, meta_b, inflate=pad, pos_a=pos_a, pos_b=None):
        return "NEAR"
    return "SAFE"


def collision_pairs_in_scene(metas: list[dict], pos_overrides: dict = None) -> list[tuple[int, int]]:
    """
    Find colliding pairs in scene.
    pos_overrides: optional dict {idx: (x, y)} to override positions for collision detection.
    """
    if pos_overrides is None:
        pos_overrides = {}
    pairs = []
    n = len(metas)
    for i in range(n):
        for j in range(i + 1, n):
            pos_i = pos_overrides.get(i, None)
            pos_j = pos_overrides.get(j, None)
            if not aabb_overlap_xy_by_meta(metas[i], metas[j], inflate=0.0, pos_a=pos_i, pos_b=pos_j):
                continue
            if obb_overlap(metas[i], metas[j], inflate=0.0, pos_a=pos_i, pos_b=pos_j):
                pairs.append((i + 1, j + 1))
    return pairs


