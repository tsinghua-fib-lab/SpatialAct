def _camera_forward_world(cam) -> Vector:
    """Get camera forward direction in world space (scene-to-camera direction)."""
    # Get camera rotation matrix
    rot_matrix = cam.matrix_world.to_quaternion().to_matrix()
    # Camera looks along -Z, so Z points opposite the viewing direction
    # Therefore camera forward is the -Z axis
    forward = rot_matrix @ Vector((0, 0, -1))
    return forward.normalized()


